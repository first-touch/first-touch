      'v-linkbutton': LinkButton
    },
    mounted() {
      this.$nextTick(() => {

        $('#landing-navbar').hide();

        $(window).scroll(function() {
          if ($(this).scrollTop() > 100) {
            return $('#landing-navbar').fadeIn();
          } else {
            return $('#landing-navbar').fadeOut();
          }
        });

        $('.more').click(function() {
          return $(this).parent().fadeOut();
        });

        $('.back').click(function() {
          return $(this).prev().fadeIn();
        });
      })
    }
  };
