<script>
import LandingNavbar from 'app/components/LandingNavbar.vue';
import LinkButton from 'app/components/LinkButton.vue';
import AOS from 'aos';
import 'aos/dist/aos.css';

AOS.init({
  duration: 2000,
});

export default {
  name: 'LandingPage',
  components: {
    'landing-navbar': LandingNavbar,
    'v-linkbutton': LinkButton,
  },
  mounted() {
    this.$nextTick(() => {

      $('.more').click(function() {
        return $(this)
          .parent()
          .fadeOut();
      });

      $('.back').click(function() {
        return $(this)
          .prev()
          .fadeIn();
      });
    });
  },
  beforeDestroy() {
    $(window).off('scroll');
  },
};

$(document).ready(function () {
  $('#toggle').carousel({
    interval: 10000
  })


  $('.carousel .carousel-item').each(function(){
    var next = $(this).next();
    if (!next.length) {
    next = $(this).siblings(':first');
    }
    next.children(':first-child').clone().appendTo($(this));

    for (var i=0;i<1;i++) {
      next=next.next();
      if (!next.length) {
        next = $(this).siblings(':first');
      }

      next.children(':first-child').clone().appendTo($(this));
    }
  });



});

</script>
